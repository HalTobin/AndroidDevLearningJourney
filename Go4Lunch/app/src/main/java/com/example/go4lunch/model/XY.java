package com.example.go4lunch.model;

public class XY {

    /** Represent the horizontal coordinate **/
    private final double x;

    /** Represent the vertical coordinate **/
    private final double y;

    public XY(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
